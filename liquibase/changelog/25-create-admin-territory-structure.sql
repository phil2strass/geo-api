--comment Convenience view to query clean administrative hierarchies with source territory metadata.

CREATE OR REPLACE VIEW v_admin_territory AS
SELECT
    at.id AS admin_territory_id,
    at.country_id,
    c.iso_code AS country_iso2,
    c.iso3_code AS country_iso3,
    at.admin_level_id,
    cal.level_number,
    cal.code AS admin_level_code,
    cal.label AS admin_level_label,
    at.territory_id,
    t.wikidata_id AS territory_wikidata_id,
    t.name AS territory_source_name,
    t.type AS territory_source_type,
    at.display_name,
    at.admin_code,
    at.admin_code_system,
    at.parent_admin_territory_id,
    parent_at.display_name AS parent_display_name,
    parent_at.admin_code AS parent_admin_code,
    parent_cal.code AS parent_admin_level_code,
    at.is_current,
    at.is_official,
    at.source
FROM admin_territory at
JOIN country_admin_level cal ON cal.id = at.admin_level_id
JOIN country c ON c.id = at.country_id
JOIN territory t ON t.id = at.territory_id
LEFT JOIN admin_territory parent_at ON parent_at.id = at.parent_admin_territory_id
LEFT JOIN country_admin_level parent_cal ON parent_cal.id = parent_at.admin_level_id;

