--comment Seed the clean French administrative hierarchy definition.

WITH france AS (
    SELECT id
    FROM country
    WHERE iso_code = 'FR'
),
seed(level_number, code, label, default_territory_type_code) AS (
    VALUES
        (1, 'region', 'Region', 'region'),
        (2, 'department', 'Departement', 'department'),
        (3, 'arrondissement', 'Arrondissement', 'district'),
        (4, 'canton', 'Canton', NULL),
        (5, 'commune', 'Commune', 'municipality')
)
INSERT INTO country_admin_level (
    country_id,
    level_number,
    code,
    label,
    default_territory_type_code,
    parent_level_id,
    is_current
)
SELECT
    f.id,
    s.level_number,
    s.code,
    s.label,
    s.default_territory_type_code,
    NULL,
    TRUE
FROM france f
CROSS JOIN seed s
ON CONFLICT (country_id, code) DO UPDATE
SET
    level_number = EXCLUDED.level_number,
    label = EXCLUDED.label,
    default_territory_type_code = EXCLUDED.default_territory_type_code,
    is_current = EXCLUDED.is_current;

WITH france AS (
    SELECT id
    FROM country
    WHERE iso_code = 'FR'
),
parent_seed(child_code, parent_code) AS (
    VALUES
        ('department', 'region'),
        ('arrondissement', 'department'),
        ('canton', 'department'),
        ('commune', 'department')
)
UPDATE country_admin_level child
SET parent_level_id = parent.id
FROM france f
JOIN parent_seed ps ON TRUE
JOIN country_admin_level parent
    ON parent.country_id = f.id
   AND parent.code = ps.parent_code
WHERE child.country_id = f.id
  AND child.code = ps.child_code;
