--comment Create per-country administrative level definitions.

CREATE TABLE IF NOT EXISTS country_admin_level (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    country_id BIGINT NOT NULL,
    level_number SMALLINT NOT NULL,
    code VARCHAR(64) NOT NULL,
    label VARCHAR(120) NOT NULL,
    default_territory_type_code VARCHAR(32),
    parent_level_id BIGINT,
    is_current BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_country_admin_level PRIMARY KEY (id),
    CONSTRAINT fk_country_admin_level_country
        FOREIGN KEY (country_id) REFERENCES country(id) ON DELETE CASCADE,
    CONSTRAINT fk_country_admin_level_parent
        FOREIGN KEY (parent_level_id) REFERENCES country_admin_level(id) ON DELETE SET NULL,
    CONSTRAINT fk_country_admin_level_default_territory_type
        FOREIGN KEY (default_territory_type_code) REFERENCES territory_type(code),
    CONSTRAINT uq_country_admin_level_country_level UNIQUE (country_id, level_number),
    CONSTRAINT uq_country_admin_level_country_code UNIQUE (country_id, code),
    CONSTRAINT chk_country_admin_level_code CHECK (code ~ '^[a-z0-9_]+$')
);

CREATE INDEX IF NOT EXISTS ix_country_admin_level_country_id
    ON country_admin_level(country_id);

CREATE INDEX IF NOT EXISTS ix_country_admin_level_parent_level_id
    ON country_admin_level(parent_level_id);

COMMENT ON TABLE country_admin_level IS
'Administrative hierarchy definition by country. This is the clean business hierarchy, separate from the broad territory gazetteer.';

COMMENT ON COLUMN country_admin_level.default_territory_type_code IS
'Optional hint for the most common territory.type backing this admin level. It is informational and not enforced.';

