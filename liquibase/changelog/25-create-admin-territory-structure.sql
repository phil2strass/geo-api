CREATE TABLE IF NOT EXISTS admin_territory (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY,
    country_id BIGINT NOT NULL,
    admin_level_id BIGINT NOT NULL,
    territory_id BIGINT NOT NULL,
    parent_admin_territory_id BIGINT,
    display_name TEXT NOT NULL,
    admin_code VARCHAR(64),
    admin_code_system VARCHAR(64),
    is_current BOOLEAN NOT NULL DEFAULT TRUE,
    is_official BOOLEAN NOT NULL DEFAULT TRUE,
    source VARCHAR(120) NOT NULL DEFAULT 'seed.country_admin_structure',
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_admin_territory PRIMARY KEY (id),
    CONSTRAINT fk_admin_territory_country
        FOREIGN KEY (country_id) REFERENCES country(id) ON DELETE CASCADE,
    CONSTRAINT fk_admin_territory_admin_level
        FOREIGN KEY (admin_level_id) REFERENCES country_admin_level(id) ON DELETE CASCADE,
    CONSTRAINT fk_admin_territory_territory
        FOREIGN KEY (territory_id) REFERENCES territory(id) ON DELETE CASCADE,
    CONSTRAINT fk_admin_territory_parent
        FOREIGN KEY (parent_admin_territory_id) REFERENCES admin_territory(id) ON DELETE SET NULL,
    CONSTRAINT uq_admin_territory_country_level_territory UNIQUE (country_id, admin_level_id, territory_id),
    CONSTRAINT uq_admin_territory_country_level_code UNIQUE (country_id, admin_level_id, admin_code_system, admin_code),
    CONSTRAINT chk_admin_territory_code_pair CHECK (
        (admin_code IS NULL AND admin_code_system IS NULL)
        OR (admin_code IS NOT NULL AND admin_code_system IS NOT NULL)
    )
);

CREATE INDEX IF NOT EXISTS ix_admin_territory_country_id
    ON admin_territory(country_id);

CREATE INDEX IF NOT EXISTS ix_admin_territory_admin_level_id
    ON admin_territory(admin_level_id);

CREATE INDEX IF NOT EXISTS ix_admin_territory_territory_id
    ON admin_territory(territory_id);

CREATE INDEX IF NOT EXISTS ix_admin_territory_parent_admin_territory_id
    ON admin_territory(parent_admin_territory_id);

COMMENT ON TABLE admin_territory IS
'Administrative territories curated on top of territory. Use this table for official country hierarchies instead of territory.type heuristics.';

COMMENT ON COLUMN admin_territory.display_name IS
'Canonical business-facing label for the administrative unit. It may be cleaner than the raw territory.name imported from Wikidata.';

COMMENT ON COLUMN admin_territory.admin_code IS
'Administrative code for the unit within the country, such as INSEE department codes in France.';

